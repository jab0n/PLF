metodo_preparacion(Planta, Metodo, Descripcion) :- planta_preparacion(Planta, Metodo), preparacion(Metodo, Descripcion).

mostrar_informacion_planta(Planta) :-
    (   hierba_medicinal(Planta, Informacion)
    ->  atomics_to_string(Informacion, ', ', InfoString),
        metodo_preparacion(Planta, Metodo, MetodoDescripcion),
        (   como_ingerir(Planta, ComoIngerir) -> true ; ComoIngerir = 'No disponible'),
        (   propiedades_planta(Planta, Propiedades) -> atomics_to_string(Propiedades, ', ', PropiedadesString) ; PropiedadesString = 'No disponibles'),
        (   elementos_planta(Planta, Elementos) -> atomics_to_string(Elementos, ', ', ElementosString) ; ElementosString = 'No disponibles'),
        (   origen_planta(Planta, Origen) -> true ; Origen = 'No disponible'),
        format('Nombre de la planta: ~w~n', [Planta]),
        format('Afecciones que trata: ~w~n', [InfoString]),
        format('Metodo de preparacion: ~w~n', [Metodo]),
        format('Preparacion: ~w~n', [MetodoDescripcion]),
        format('Como ingerir: ~w~n', [ComoIngerir]),
        format('Propiedades: ~w~n', [PropiedadesString]),
        format('Elementos: ~w~n', [ElementosString]),
        format('Origen: ~w~n', [Origen])
    ;   write('Planta no encontrada'), nl
    ).

mostrar_informacion_remedio(Remedio) :-
